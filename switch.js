let grade = 'A';


switch (grade) {
    case 'A':
       console.log ( "Excellent");
   
    case 'B':
         console.log  ("Good");
        

    
    case 'C':
        console.log ( "Average");
       
    case 'D':
       console.log ("Poor");
 
    case 'F':
        console.log ("Fail");
        break;
     
    default:
        console.log  ("Invalid grade entered");
}
